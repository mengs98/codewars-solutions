/*
==========
CHALLENGE
==========
This code does not execute properly. Try to figure out why!

function multiply(a, b){
  a * b
}
*/

/*
===========
SOLUTION 1
===========
The function needs to return something. Add keyword 'return' in the function body

function multiply(a, b) {
  return a * b
}
*/

/*
===========
SOLUTION 2
===========
With arrow function, we don't need to explicitly write 'return' keyword for returning value
*/
const multiply = (a, b) => a * b;